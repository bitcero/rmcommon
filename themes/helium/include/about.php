        <h3 class="text-success">Crear tus propios colores</h3>
        <p>
            Puedes crear tus propios esquemas de colores para <strong>Two·P6</strong>. Para hacerlo debes crear un nuevo archivo y guardarlo
            en el directorio <code>twop6/css/schemes</code>. Es una buena idea, para empezar, editar uno de los archivos existentes.
        </p>
        <p>
            <span class="fa fa-info-circle text-info"></span>
            La primera línea del archivo deben contener el nombre del nuevo esquema y este debe tener una longitud máxima de 60 caracteres. Ejemplo:
        </p>
        <pre><span class="text-success">/* <strong>My Color Scheme Name</strong> */</span>
body{
    ...
}</pre>

        <h3 class="text-info">Adaptando las plantillas de tú módulo</h3>
        <p>
            Si deseas utilizar todas las capacidades de Two·P6 en las plantillas de tu módulo, solo debes seguir las mismas reglas de Bootstrap 3
            y todo fluirá naturalmente.
        </p>
        <p>
            Otros elementos, como la barra de herramientas y la integración pueden ser realizadas a través de las características de Common
            Utilities.
